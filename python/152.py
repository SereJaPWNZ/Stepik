# –ü—Ä–∏–º–µ—á–∞–Ω–∏–µ. –ï—Å–ª–∏ —Ç–∞–∫–∏—Ö —á–∏—Å–µ–ª –Ω–µ—Å–∫–æ–ª—å–∫–æ, —Ç–æ –≤—ã–≤–µ–¥–∏—Ç–µ –Ω–∞–∏–±–æ–ª—å—à–µ–µ –∏–∑ –Ω–∏—Ö.

a, b, sdel = int(input()), int(input()), 0
for i in range(a, b + 1):
    s = i
    for j in range(1, i // 2 + 1):
        if i % j:
            continue
        s += j
    if s >= sdel:
        sdel = s
        maxn = i
print(maxn, sdel)