# # Две старушки
# # Две старушки идут навстречу друг другу с постоянными скоростями v1 and v2 км/ч. Определите, через какое время старушки встретятся, если расстояние между ними равно S км.
# Формат входных данных
# На вход программе подаются три числа с плавающей точкой S,V1 ,V2, каждое на отдельной строке.
# Формат выходных данных 
# Программа должна вывести одно число в соответствии с условием задачи.
